	__HAL_TIM_SET_COUNTER(&htim4,differ);
	HAL_TIM_Base_Start(&htim4);					//启动定时器
	while(differ<0xffff-6)						//补偿，判断
	{
		differ=__HAL_TIM_GET_COUNTER(&htim4);			//查询计数器的计数值
	}
